package com.java.class05;

// Program to check given char is vowel or not
// vowel - a, e, i, o, u

public class CheckVowel {
	public static void main(String[] args) {
		char c = 'r';

		if (c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u') {
			System.out.println("Vowel");
		} else {
			System.out.println("Not Vowel");
		}

	}
}
