package com.java.class03;

public class ModuloOperator {
	public static void main(String args[]) {

		System.out.println(39 % 5);
		System.out.println(39 % 4);

		// Odd Even
		System.out.println(41 % 2);
		System.out.println(89 % 2);
		System.out.println(14 % 2);
		System.out.println(28 % 2);

		// Retrieve the last digit
		System.out.println(41 % 10);
		System.out.println(89 % 10);
		System.out.println(14 % 10);
		System.out.println(28 % 10);
		System.out.println(255 % 10);

		// Reminder will be the whole number
		System.out.println(9 % 10);

		System.out.println(9 + 10);
		System.out.println(9 - 10);
		System.out.println(9 * 10);
		System.out.println(1 / 2);

		System.out.println(5 / 2.0);

		System.out.println(89 / 10);
		System.out.println(254 / 10);
		System.out.println(98 / 10);
		System.out.println(71 / 10);

	}
}
